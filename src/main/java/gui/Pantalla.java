    private void btnConvertActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnConvertActionPerformed
        String importe = importField.getText();
        double convertedImporte = Double.parseDouble(importe);

        // Get the selected currencies
        String initial = (String) initialCurrency.getSelectedItem();
        String finalCur = (String) finalCurrency.getSelectedItem();

        double rate = getRate(initial, finalCur);
        double result = convertedImporte * rate;
        // Set the result in the resultField
        resultField.setText(String.format("%.2f", result));
    }//GEN-LAST:event_btnConvertActionPerformed

